void loadConfig() {
  if (!LittleFS.exists("/config.json")) {
    Serial.println("settings.json does not exist");
    return;
  }

  File file = LittleFS.open("/config.json", "r");
  if (!file) {
    Serial.println("reading error settings.json");
    return;
  }

  StaticJsonDocument<256> jsonDocConfig;
  DeserializationError error = deserializeJson(jsonDocConfig, file);
  file.close();

  if (error) {
    Serial.println("parsing error settings.json");
    return;
  }

  // Variablen laden
  name = (const char*) jsonDocConfig["Name"];
  staSSID = (const char*) jsonDocConfig["WifiSSID"];
  staPassword = (const char*) jsonDocConfig["WifiPW"];
  staIP = (const char*) jsonDocConfig["staIP"];
  network = (bool) jsonDocConfig["network"];
  MQTTtopic = (const char*) jsonDocConfig["MQTTtopic"];
  MQTTname = (const char*) jsonDocConfig["MQTTname"];
  screenRoataion = (int) jsonDocConfig["ScreenOrientation"];
  LedSwitch = (bool) jsonDocConfig["LED"];
  resetDelay = (int) jsonDocConfig["DisplayTimeout"];
  tempUnit = (uint8_t) jsonDocConfig["Unit"];

  Serial.println("Loaded");
}

void touchCalibrate() { // calibrate the Touchpin
  int touchValue = touchRead(TOUCH_PIN); // Baseline
  touchTreshhold = touchValue + touchSense;
  touchAttachInterrupt(TOUCH_PIN, onTouch, touchTreshhold); // Touchinterrupt
  Serial.print("Touch Calibrated - Baseline: ");
  Serial.print(touchValue);
  Serial.print(" New treshold: ");
  Serial.println(touchTreshhold);
  }

